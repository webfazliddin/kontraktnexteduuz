  components: { Myinfo, Applications, BIconFunnel },
  data() {
    return {
      GetData: {},
      loading: false
    };
  },
  async created() {
    await this.Refresh();
  },
  methods: {
    makeToast(message, type) {
      var a = "";
      if (message.status == 500) {
        a = message.title;
      }
      if (message.status == 400) {
        var errors = Object.values(message.errors);
        var a = errors.map((el, item) => item + 1 + "." + el).join("\n");
      } else {
        a = message;
      }

      this.$toast.open({
        message: a,
        type: type,
        duration: 5000,
        dismissible: true
      });
    },
    Refresh() {
      this.loading = true;
      MainPageService.GetInfo()
        .then(res => {
          this.GetData = res.data;
          this.loading = false;
        })
        .catch(error => {
          if (error.response.data.status == 500) {
            this.makeToast(this.$t("errormessage500"), "error");
            this.loading = false;
          } else {
            this.makeToast(error.response.data, "error");
            this.loading = false;
          }
        });
    }
  }
};
